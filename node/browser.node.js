/* addEnv/browser代码引入 */
const fs = require('fs');
const path = require('path');


let browserPath = path.resolve(__dirname, "../addEnv/browser");
let fileNameList = [
    ///////////////////////////////////////////////////////////////////////////////////////  Event  ///////////////////////////////////////////////////////////////////////////////////////
    "Event.js", "UIEvent.js", "KeyboardEvent.js",
    ///////////////////////////////////////////////////////////////////////////////////////  Event  ///////////////////////////////////////////////////////////////////////////////////////

    ///////////////////////////////////////////////////////////////////////////////////////  EventTarget  ///////////////////////////////////////////////////////////////////////////////////////
    "EventTarget.js", 
    ///////////////////////////////////////////////////////////////////////////////////////  EventTarget  ///////////////////////////////////////////////////////////////////////////////////////

    ///////////////////////////////////////////////////////////////////////////////////////  BOM  ///////////////////////////////////////////////////////////////////////////////////////
    "Crypto.js", "History.js", "Location.js", "Screen.js", "Storage.js", "Navigator.js", "Window.js", "XMLHttpRequestEventTarget.js", "XMLHttpRequest.js",
    ///////////////////////////////////////////////////////////////////////////////////////  BOM  ///////////////////////////////////////////////////////////////////////////////////////

    ///////////////////////////////////////////////////////////////////////////////////////  DOM  ///////////////////////////////////////////////////////////////////////////////////////
    "Node.js", "DOMParser.js", "Element.js", "Document.js", "HTMLDocument.js", "HTMLElement.js", "HTMLDivElement.js",
    ///////////////////////////////////////////////////////////////////////////////////////  DOM  ///////////////////////////////////////////////////////////////////////////////////////
];


function getCode() {
    let code = "";
    fileNameList.forEach(fileName => {
        code += fs.readFileSync(`${browserPath}/${fileName}`) + '\r\n\r\n\r\n';
    });
    return code;
}


module.exports = {
    getCode
}